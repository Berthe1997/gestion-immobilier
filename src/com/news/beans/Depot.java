package com.news.beans;

public class Depot {
	private Long id;
	private String site;
	private String ans;
	private int montantD;
	private int montantR;
	private String date;
	private String heur;
	private String mois;
	private String code;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getSite() {
		return site;
	}
	public void setSite(String site) {
		this.site = site;
	}
	public String getAns() {
		return ans;
	}
	public void setAns(String ans) {
		this.ans = ans;
	}
	public int getMontantD() {
		return montantD;
	}
	public void setMontantD(int montantD) {
		this.montantD = montantD;
	}
	public int getMontantR() {
		return montantR;
	}
	public void setMontantR(int montantR) {
		this.montantR = montantR;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getHeur() {
		return heur;
	}
	public void setHeur(String heur) {
		this.heur = heur;
	}
	public String getMois() {
		return mois;
	}
	public void setMois(String mois) {
		this.mois = mois;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	
}
