package com.news.beans;

public class Charges_entretiens {

	private Long id;
	private String propriete;
	private String type_charge;
	private int montantD;
	private String ans;
	private String mois;
	private String date;
	private int montantDM;
	private String porte;
	private String code;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getPropriete() {
		return propriete;
	}
	public void setPropriete(String propriete) {
		this.propriete = propriete;
	}
	public String getType_charge() {
		return type_charge;
	}
	public void setType_charge(String type_charge) {
		this.type_charge = type_charge;
	}
	public int getMontantD() {
		return montantD;
	}
	public void setMontantD(int montantD) {
		this.montantD = montantD;
	}
	public String getAns() {
		return ans;
	}
	public void setAns(String ans) {
		this.ans = ans;
	}
	public String getMois() {
		return mois;
	}
	public void setMois(String mois) {
		this.mois = mois;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getMontantDM() {
		return montantDM;
	}
	public void setMontantDM(int montantDM) {
		this.montantDM = montantDM;
	}
	public String getPorte() {
		return porte;
	}
	public void setPorte(String porte) {
		this.porte = porte;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
}
