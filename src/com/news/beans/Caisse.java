package com.news.beans;

public class Caisse {

	private Long id;
	private String code;
	private String codeCaisse;
	private String libCaisse;
	private String calCaisse;
	private int of;
	private String commentaire;
	private String date;
	
	public Long getId() { return id; }
	public void setId(Long id) { this.id = id; }

	public String getCode() { return code; }
	public void setCode(String code) { this.code = code; }
	
	public String getCodeCaisse() { return codeCaisse; }
	public void setCodeCaisse(String codeCaisse) { this.codeCaisse = codeCaisse; }
	
	public String getLibCaisse() { return libCaisse; }
	public void setLibCaisse(String libCaisse) { this.libCaisse = libCaisse; }
	
	public String getCalCaisse() { return calCaisse; }
	public void setCalCaisse(String calCaisse) { this.calCaisse = calCaisse; }
	
	public int getOf() { return of; }
	public void setOf(int of) { this.of = of; }
	
	public String getCommentaire() { return commentaire; }
	public void setCommentaire(String commentaire) { this.commentaire = commentaire; }
	
	public String getDate() { return date; }
	public void setDate(String date) { this.date = date; }
	
	
}
