public class Declaration_police {

	private Long id;
	private String agence;
	private String code_reservation;
	private String date_declaration;
	private String statut_transmition;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getAgence() {
		return agence;
	}
	public void setAgence(String agence) {
		this.agence = agence;
	}
	public String getCode_reservation() {
		return code_reservation;
	}
	public void setCode_reservation(String code_reservation) {
		this.code_reservation = code_reservation;
	}
	public String getDate_declaration() {
		return date_declaration;
	}
	public void setDate_declaration(String date_declaration) {
		this.date_declaration = date_declaration;
	}
	public String getStatut_transmition() {
		return statut_transmition;
	}
	public void setStatut_transmition(String statut_transmition) {
		this.statut_transmition = statut_transmition;
	}
	
}
